import express from 'express';
import { jsonValidation, sendToDummy } from '../services/dummyService';

const router = express.Router();

router.post('/', (req, res) => {
    (async () => {
        if (jsonValidation(req.body)) {
            const response = await sendToDummy(req.body);
            res.send(response);
        } else {
            res.status(400).json({
                error: 'Bad request, json did not pass basic validation',
            });
        }
    })().catch(() => 'obligatory catch to please eslinter');
});

router.get('/*', (req, res) => {
    res.send(req.path);
});

export default router;

